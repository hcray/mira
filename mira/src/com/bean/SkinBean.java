package com.bean;

public class SkinBean {
	
	/**
	 * 皮肤类型编号
	 */
	private String skinId;
	
	/**
	 * 皮肤类型名称
	 */
	private String name;

	public String getSkinId() {
		return skinId;
	}

	public void setSkinId(String skinId) {
		this.skinId = skinId;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}
	
}
